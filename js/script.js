function softSkills() {
  var x = document.getElementById("mySoftSkills");
  if (x.style.display === "block") {
    x.style.display = "none";
  } else {
    x.style.display = "block";
  }
}

function hardSkills() {
  var x = document.getElementById("myHardSkills");
  if (x.style.display === "block") {
    x.style.display = "none";
  } else {
    x.style.display = "block";
  }
}
